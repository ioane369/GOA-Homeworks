# This function takes two numbers as input and returns the result of their substraction.

def subtraction(num1, num2):
    return num1 - num2


# This function takes two numbers as input and returns the result of their addition.

def addition(num1, num2):
    return num1 + num2



# This function takes two numbers as input and returns the result of their division.

def division(num1, num2):
    return num1 / num2



# This function takes two numbers as input and returns the result of their multiplication.

def multiplication(num1, num2):
    return num1 * num2


# Calling all the functions

print(subtraction(10, 5))

print(addition(1, 2))

print(division(6, 3))

print(multiplication(2, 10))